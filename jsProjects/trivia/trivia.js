let counterSuccess = 0;
let counterAttempts = 0;
let currentQuestion = {};
let timeLeft = 60;
let timer;

function loadQuestion() {
  let randomIndex = Math.floor(Math.random() * questionsBank.length);
  currentQuestion = questionsBank[randomIndex];

  document.getElementById("theQuestion").textContent = currentQuestion.question;
  let buttons = document.querySelectorAll("#theAnswers button");
  buttons.forEach((button) => {
    let choice = button.getAttribute("data");
    button.textContent = currentQuestion[choice];
  });
}

function checkAnswer(event) {
  let selectedAnswer = event.target.getAttribute("data");
  if (selectedAnswer === currentQuestion.answer) {
    counterSuccess++;
    alert("Correct!");
  } else {
    alert("Wrong! The answer is:" + currentQuestion.answer);
  }
  counterAttempts++;
  document.getElementById(
    "attempts"
  ).textContent = `Attempts: ${counterAttempts}, Correct: ${counterSuccess}`;
  loadQuestion();
}

function startTimer() {
  timer = setInterval(() => {
    timeLeft--;
    document.getElementById("timer").textContent = `Time Left: ${timeLeft}s`;
    if (timeLeft <= 0) {
      clearInterval(timer);
      alert("Game Over! Time is up.");
      resetGame();
    }
  }, 1000);
}

function resetGame() {
  counterSuccess = 0;
  counterAttempts = 0;
  timeLeft = 60;
  document.getElementById(
    "attempts"
  ).textContent = `Attempts: ${counterAttempts}, Correct: ${counterSuccess}`;
  document.getElementById("timer").textContent = `Time Left: ${timeLeft}s`;
  loadQuestion();
  startTimer();
}

document.querySelectorAll("#theAnswers button").forEach((button) => {
  button.addEventListener("click", checkAnswer);
});

window.onload = () => {
  loadQuestion();
  document.getElementById(
    "attempts"
  ).textContent = `Attempts: ${counterAttempts}, Correct: ${counterSuccess}`;
  document.body.insertAdjacentHTML(
    "beforeend",
    '<div id="timer" style="font-size:1.2em; margin-top:10px; font-weight:bold; color:#d32f2f;">Time Left: 60s</div>'
  );
  startTimer();
};
